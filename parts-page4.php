														<div align="justify">
															<font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><strong>Flow / No Flow Detection for Solids and Powders</strong> <br />
																<br />
																The flow detect system consists of two components &#150; the FDS1000 Remote Sensor Probe which is mounted in a pneumatic pipeline, gravity chute or feeder &#150; and the FDC1000 Control Console which is mounted in an area accessible for users to read the console during operations. The system promotes continuous and efficient operations by informing users that solid or powder materials are flowing and alerts them if the flow status has changed, power has been lost, or if communication between the Remote Sensor Probe and the Control Console has been interrupted.<br />
																<br />
																The FDS 1000 Remote Sensor Probe is a high quality, industrial grade instrument that senses flow / no-flow conditions using Doppler technology (microwave) to provide highly reliable and sensitive motion detection. It works by transmitting a low energy signal through a Teflon process seal into the material flow stream. A portion of the signal is reflected back to the sensor, with the movement of material causing a frequency shift &#150; called the Doppler shift &#150; which is used by the sensor to detect material flow.<br />
