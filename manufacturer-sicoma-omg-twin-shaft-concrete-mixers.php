      perfecting the spiral mixing action that forces the mix to circulate in a counterclockwise direction, allowing the ISO target of 95% homogeneity to 
      
      be reached within 30 seconds and a 98.5% homogeneity in 60 seconds. The result is a faster and better mixed batch, both for regular slump 
      
      concrete and SCC.
