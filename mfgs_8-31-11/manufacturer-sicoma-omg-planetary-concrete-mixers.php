                                                <h2>Planetary / Countercurrent Concrete Mixers</h2>
<p>The Sicoma high speed planetary / countercurrent concrete mixer is the best in its class. There are <u>more than 8000 Sicoma high speed planetary / countercurrent concrete mixers in use today around the world</u>.</p>
<img src="http://www.gulfatlanticequipment.com/manfact-images/planetary-interior.jpg" alt="planetary concrete mixer interior" />			             
<p>The Sicoma planetary (or countercurrent) design has one or two mixing stars that rotate themselves while also rotating around a central point, allowing each arm to cover the whole mixer floor in a number of revolutions. <u>The Sicoma's powerful mixing action is both fast and thorough, results in a mixing time, after water is added, of 30 seconds or less for most types of concrete</u>. The result is greater throughput, providing more concrete per hour for the same size plant, as well as better consistency and greater strength or, on the other hand, a saving on cement.<br />
 
Unlike others planetary mixers of similar design, the Sicoma paddles and gears are arranged so <u>every inch of the mixer floor is swept with high repeatability over 60 seconds of operation</u>; each arm sweeping an inch or so behind its previous path and alternate arms covering the gaps so that, even with blades removed from the arms, every particle of concrete would be moved. The consequence of the Sicoma planetary concrete mixer is that the mixing action is thorough and the blades do not have to be oversized to do it. Additionally, discharge is fast and complete without the need for a wide scraper tail which creates dead spots in the mixer.</p>
<h2>Mixing Blades</h2>
 
<img src="http://www.gulfatlanticequipment.com/manfact-images/planetary-paddles.jpg" alt="planetary concrete mixing paddles" hspace="20" align="right" />
<p>The Sicoma <u>Ni-Hard cast iron blades are extremely hard wearing</u>; they are also easy to adjust and replace.<br />
  
In addition, the <u>Sicoma blades are wear-equalized</u>; compared with other, symmetrical designs, Sicoma's blades are thicker on the outside edge to exactly compensate the extra wear at these points. Optional rubber, chromium carbide and tungsten carbide blades are available for high wear and special applications.</p>
<h2>Mixing Cycle</h2>
<p>Although the total cycle time is dependent on the speed at which charging takes place as well as the type of concrete being produced, <u>typically a batch of wet cast concrete can be produced in less than 110 seconds</u>, with dry cast, block and paver mixes taking a little longer, depending on the type of moisture/water dosing system employed.  Note that some admixtures require extra mixing time.</p> 


<img src="http://www.gulfatlanticequipment.com/manfact-images/planet3.jpg" alt="high pressure wash out" width="208" height="120" hspace="20" vspace="10" align="right" />
<h2>High pressure washout</h2>
  
<p>The standard water outlets are spaced around a 180-1/2" inch spreader bar, angled to give best water distribution.  High-pressure washout adds spray jets, angled to cover the whole mixer interior, mounted on the underside of the rotating mixing arms.  Sicoma's concrete mixer washout system is powered by a powerful high-volume pump.  The pump can be shared with manual spray wand.</p>
<h2>Discharge Doors Run in Rubber Seals</h2>
<p>Rubber sealed discharge doors run smoothly and eliminate leakage of water.  They are watertight, non-jamming, and require low maintenance - reducing lifetime cost.  All models are hydraulically powered, but for smaller mixers, both pneumatic or manual operations are available.</p>
