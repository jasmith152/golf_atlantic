                  <p><img src="manfact-images/wam-pic10.jpg" height="100" width="105" align="right" border="0" v:shapes="pix" /><font size="2" face="Arial, Helvetica, sans serif">Pressure Relief Valves are the last safety net when abnormal pressure conditions endanger the silo structure. This is why sudden excess or suction pressure inside the silo must be dealt with instantaneously. Even though ideally a Pressure Relief Valve should never have to go into action, it must be efficient and reliable if needed.</font></p>
                 </div>
                 <div align="justify">
                  <p><font size="2" face="Arial, Helvetica, sans serif">In the Pressure Relief Valve, helical springs keep the valve lids closed when the pressure value remains within the preset limits. The three outside spring rods keep the external ring-shaped lid firmly closed as long as the force generated by the pressure inside the silo does not overcome the spring force. Once the pressure exceeds the preset value the lid is pushed up and the pressure can escape. The smaller lid covers the central circular opening of the external lid from below. It is held in the middle by a single spring rod and is pressed onto the external lid by the normal air pressure inside the silo. In the event of suction pressure, the spring is compressed and allows the lid to drop. The air entering the silo from outside ensures rapid pressure balance and pushes the central lid back up into the &quot;closed&quot; position.<br />
