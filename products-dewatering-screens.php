														<h2>More information on sludge dewatering Systems</h2>
														<div align="justify">
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>MOTOR AND DRIVE ASSEMBLY</b><br />
																	The shaft mounted gear reduction is totally enclosed within an oil bath gear box. The reduction is designed with easy service and customer convenience in mind. By simply changing the drive pulley on the motor, you may increase or decrease the speed to custom tailor the washing action on your material. All standard electric motors are 230/460 Volt, 3-phase, 60 cycle. Parts required for routine maintenance are standard off-the-shelf items and are easily found in most metropolitan areas nationwide.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>SHAFT-MOUNTED GEAR BOX DRIVE</b><br />
																	A greatly improved drive is used on all GreyStone washers. Based on the use of standard, direct, shaft-mounted speed reducers, the drive provides several significant advantages.</font></p>
															<ul>
																<li><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular">The drive is fully enclosed, with all gears running in a sealed oil bath.</font></li>
																<li><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular">Maintenance is economical since only stock reducers are used.</font></li>
																<li><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular">The drive is simplified by reducing the number of shafts, bearings,<br />
																		&nbsp;and other components.</font></li>
																<li><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular">The transmission is highly efficient because of the direct shaft arrangement,<br />
																		&nbsp;&nbsp; with V-belt drive to electric motor and belt guard.</font></li>
																<li><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular">Falk or Dodge shaft-mounted gear reduction drives are used and<br />
																		&nbsp;&nbsp; are readily available.</font></li>
															</ul>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>SHAFTING</b><br />
																	The heavy stub of mirror-smooth, heat-treated shafting is fitted into a hard stainless steel spacing sleeve. Shaft and sleeve are driven through a heavy duty rubber gasket that fits snugly between the connecting disc and tub. A specially designed slinger fits perpendicular to the shaft between the roller bearing and sleeve and turns with the shaft adding further protection to the outside mounted bearing from the seal leakage which may follow extensive use.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>SHAFT AND FLIGHTS</b><br />
																	The spiral-type, half pitch steel flights are welded to a heavy tubular steel shaft. Each spiral screw flight is formed by a special machine to ensure a uniform spiral. The shaft flanges are precision ground after they have been welded onto the heavy tubular steel shaft.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>ADJUSTABLE SCREW SHAFT</b><br />
																	The adjustable screw shaft makes a unique design. This will allow you to control the clearance between the belly pan and screw flights...Exclusively from GreyStone.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>ADJUSTABLE WEAR SHOES</b><br />
																	Heavy-duty 1-inch-thick rock-grade rubber wear shoes are used on GreyStone Washers. These shoes can be adjusted 3/4&quot; for years of additional service. Rubber will outwear other material at approximately half the replacement cost.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>EXTERNAL BEARING</b><br />
																	GreyStone Sand Dewatering Screws have externally mounted pillow block bearings that are standard, off-the-shelf components for easy replacement.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>LARGE HEAVY-DUTY TUB</b><br />
																	Strong and rigidly constructed screw washers are designed to provide years of maintenance-free service. GreyStone tubs are all welded construction and are made of heavy steel plate with angle iron skid frame. GreyStone machines have a heavy belly pan which is curved to fit tightly to the screw shaft. During operation, this feature keeps sand from slipping back and increases your production.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>THREE-SIDE OVERFLOW </b><br />
																	GreyStone's large tub gives maximum weir length and maximum sand recovery. The GreyStone classifier features a three-side overflow flume with adjustable weirs. Weirs can be set to control the amount of fines to be retained or rejected. Efficient overflow distribution assures quiet pool area to prevent fine sand from flushing out with waste water.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>FEED BOX AND SKIRT </b><br />
																	The feed box is designed to effectively control the velocity of the aggregate being introduced into the screw tub. This will reduce turbulence within the pool area and allow a uniform flow of material.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>RISING CURRENT BELLY PAN</b><br />
																	The rising current water enters the pool from the bottom and filters up, separating fines, dirt and other extraneous matter, which is carried to the top and forced out with the overflow.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>CLEAN-OUT DRAIN</b><br />
																	An easily operated clean-out valve is provided for complete draining of the reservoir on the lower end of the tub.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>HEAVY-DUTY SKID FRAME</b><br />
																	GreyStone standard series washers are equipped with a heavy-duty skid frame. Pull a GreyStone to where it is needed without using a crane&#x2026;an Exclusive GreyStone Feature.</font></p>
