														<h2>Dewatering Equipment Info</h2>
														<div align="left">
															<p><a name="motor" id="motor"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>MOTOR AND DRIVE ASSEMBLY<br />
																	</b>The shaft-mounted gear reduction is totally enclosed within an oil-bath gear box. The reduction is designed with easy service and customer convenience in mind. By simply changing the drive pulley on the motor, you may increase or decrease the speed to custom-tailor the washing action on your material. All electric motors are 230-460 Volt, 3-phase. Parts required for routine maintenance are standard, off-the-shelf components for easy replacement.</font></p>
															<p><a name="gearbox" id="gearbox"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>SHAFT-MOUNTED GEAR BOX DRIVE</b><br />
																	A greatly improved drive is used on all GreyStone washers. Based on the use of standard, direct, shaft-mounted speed reducers, the drive provides several significant advantages.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>1.</b> The drive is fully enclosed, with all gears running in a sealed oil bath.<br />
																	<b>2.</b> Maintenance is economical since only stock reducers are used.<br />
																	<b>3.</b> The drive is simplified by reducing the number of shafts, bearings,<br />
																	&nbsp;&nbsp;&nbsp;&nbsp;and other components.<br />
																	<b>4.</b> The transmission is highly efficient because of the direct shaft arrangement,<br />
																	&nbsp;&nbsp;&nbsp; with V-belt drive to electric motor and belt guard.<br />
																	<b>5.</b> Falk or Dodge shaft-mounted gear reduction drives are used<br />
																	&nbsp;&nbsp;&nbsp; and are readily available.</font></p>
															<div align="justify"><p><a name="shafting" id="shafting"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>SHAFTING</b><br />
																	The heavy stub of mirror-smooth, heat-treated shafting is fitted into a stainless steel spacing sleeve. Shaft and sleeve are driven through a heavy duty rubber gasket that fits snugly between the connecting disc and tub. A specially designed slinger fits perpendicular to the shaft between the roller bearing and sleeve and turns with the shaft, adding further protection to the outside mounted bearing from the seal leakage which may follow extensive use.</font></p>
															<p><a name="flights" id="flights"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>SHAFT AND FLIGHTS</b><br />
																	The spiral-type, half pitch steel flights are welded to a heavy tubular steel shaft. Each spiral screw flight is formed by a special machine to ensure a uniform spiral. The shaft flanges are precision ground after they have been welded onto the heavy tubular steel shaft.</font></p>
															<p><a name="screw" id="screw"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>ADJUSTABLE SCREW SHAFT</b><br />
																	The adjustable screw shaft makes a unique design. This will allow you to control the clearance between the belly pan and screw flights.</font></p>
															<p><a name="wearshoes" id="wearshoes"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>STANDARD NI-HARD ADJUSTABLE WEAR SHOES</b><br />
																	Ni-Hard Wear Shoes are standard on GreyStone Coarse Material Washers.</font></p>
															<p><a name="paddle" id="paddle"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>ADJUSTABLE PADDLE</b><br />
																	GreyStone paddles are made of heavy duty Ni-Hard. Paddle mounting bases are constructed to give years of trouble-free service life. Water is induced into the tub through the perforated belly pan. Sufficient water should be used to wash out and carry extraneous material out over the overflow flume at the lower tub end. Soluble clay and dust from gravel or crushed stone are eliminated from the material by this abrading washing action. For tougher washing requirements, more paddles can be added to the screw shaft to replace the flights. In this application, the material which is fed into the washer will be retained longer in the scrubbing area for more thorough cleaning. With the addition of more paddles on the screw shaft, your capacity will be reduced and horsepower requirements will be increased. GreyStone Coarse Material Washers are standard 1/2 pitch screw flights. The 2nd and 3rd screw flights are replaced with paddles. If additional paddles are needed to meet your job requirements, the 5th and 6th screw flights can be replaced with our optional paddle arrangement. For specific or severe washing problems that cannot be accomplished with a coarse material washer, please refer to the complete GreyStone line of Log Washers.</font></p>
															<p><a name="bearing" id="bearing"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>EXTERNAL BEARING</b><br />
																	GreyStone Coarse Material Washers have externally mounted pillow block bearings that are standard, off-the-shelf components for easy replacement.</font></p>
															<p><a name="tub" id="tub"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>LARGE HEAVY DUTY TUB</b><br />
																	Strong and rigidly constructed screw washers are designed to provide years of maintenance-free service. GreyStone tubs are all welded construction and are made of heavy steel plate with angle iron skid frame. GreyStone machines have a heavy belly pan which is curved to fit tightly to the screw shaft. During operation, this feature keeps sand from slipping back and increases your production. This feature allows you to make a field adjustment of the clearance between the spiral flight and the belly pan for closer tolerances.</font></p>
															<p><a name="bellypan" id="bellypan"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>RISING CURRENT BELLY PAN</b><br />
																	The rising current water enters the pool from the bottom and filters up, separating fines, dirt and other extraneous matter, which is carried to the top and forced out with the overflow.</font></p>
															<p><a name="skid" id="skid"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>HEAVY DUTY SKID FRAME</b><br />
																	GreyStone washers are equipped with a heavy duty skid frame. Pull a GreyStone to where it's needed without using a crane&#x2026;an Exclusive GreyStone Feature.</font></p>
