															<p><a name="shafting" id="shafting"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>SHAFTING</b><br />
																	The heavy stub of mirror-smooth, heat-treated shafting is fitted into a hard stainless steel spacing sleeve. Shaft and sleeve are driven through a heavy-duty rubber gasket that fits snugly between the connecting disc and tub. A specially designed sling fits perpendicular to the shaft between the roller bearing and sleeve. It turns with the shaft further protecting the exterior mounted bearing from seal leakage.</font></p>
															<p><a name="bearing" id="bearing"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>EXTERNAL BEARING</b><br />
																	GreyStone Log Washers have externally mounted pillow block bearings on the lower end that are standard off-the-shelf items available in all metropolitan cities nationwide.</font></p>
															<p><a name="paddle" id="paddle"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>PADDLE DESIGN</b><br />
																	GreyStone Log Washers have a superior design in which paddles are positioned in a spiral along the log shaft. The spiral action will eliminate intermittent shock loading on the shaft and gear box. Other log washers using in-line paddle placement are confronted with increased energy requirements, vibration and continuous shock load on the drive system. The GreyStone spiral design will give continuous motion in the material, resulting in maximum scrubbing action. The material being agitated in a continuous motion will reduce energy requirements.</font></p>
															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><a href="#top">Back to Top</a></font></p>
															<p><a name="wearshoes" id="wearshoes"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>ADJUSTABLE NI-HARD WEAR PADDLES, PADDLE BASE, AND LOG SHAFTS</b><br />
																	The paddle bases have a unique design which allows extra wear life. The bases have two sets of bolt holes which will allow you to move the Ni-Hard wear-resistant paddles out after the ends have been worn off. Ni-Hard wear paddles provide maximum service life with minimum maintenance requirements. This will give extra life from these wear shoes. GreyStone wear shoe paddles are mounted to the bases with Grade 8 bolts. The paddle bases are installed at the proper angle to the extra-heavy seamless steel pipe. This will give maximum washing action with the highest possible production. The heavy seamless steel log shafts have flanges welded to both ends. After these flanges have been installed they are precision ground to provide you with maximum bearing, gear and shaft service.</font></p>
															<p><a name="tub" id="tub"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>LARGE HEAVY-DUTY TUB</b><br />
																	GreyStone designs strong and rigidly constructed Log Washers to provide years of maintenance-free service. Each GreyStone Log Washer is all-welded construction made of heavy plate steel with angle iron skid frame to reduce moving and set up time, protect bearing, gear box and V-Belt drive, and give rigidity during operation.</font></p>
															<p><a name="frame" id="frame"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>HEAVY-DUTY SKID FRAME</b><br />
																	GreyStone standard series washers are equipped with a heavy-duty skid frame. Pull a GreyStone to where it is needed without using a crane... an Exclusive GreyStone Feature.</font></p>
															<p><a name="injection" id="injection"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>RISING CURRENT INJECTION</b><br />
																	The rising current water enters the Log Washer tub from the bottom and filters up, separating fines, dirt and other extraneous matter, which is carried to the top and forced out with the overflow. The water is injected between the log shaft through a perforated dome-shaped injection system. Additional water may be added through spray bars mounted along the top of the tub. It is recommended that aggregate be pressure sprayed on a screen following the log wash.</font></p>
															<p><a name="drain" id="drain"></a><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>CLEAN-OUT DRAIN</b><br />
																	An easily operated clean-out valve is provided on the lower end of the tub for flushing and draining of the tub reservoir.</font></p>
