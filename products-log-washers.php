															<p><font size="2" face="Arial, Helvetica, Geneva, Swiss, SunSans-Regular"><b>GEAR AND DRIVE ASSEMBLY</b><br />
																	The GreyStone gear reduction is totally enclosed within an oil bath gear box. The Log Washer shafts are driven by twin helical, synchronized precision gears. The gear reduction is accomplished with only two sets of reducer gears. All shafts are mounted in flange bearings which are standard off-the-shelf items. The lower bearings are all externally mounted and can be pressure-lubricated with ease. This unique reduction is designed with service and customer convenience in mind.</font></p>
