              <img src="http://www.gulfatlanticequipment.com/manfact-images/batchfeeding-4.jpg" alt="sicoma batch feeding system" hspace="20" align="right" /><p>To obtain the fastest mixing cycles and maximum throughput for the size of mixer, it is essential to feed the mixer at high speed. This makes a skip hoist or holding hopper essential if the plant is a low profile design with aggregate scale at ground level or lower. An inclined belt, pocket belt or bucket elevator can lift the aggregates to the level of the holding hopper. If the plant is high profile, vertical or tower type, the aggregate holding hopper can be mounted on load cells to become a scale. We can advise on the design as well as supplying the equipment from one of our partner companies, giving you a guaranteed solution.
